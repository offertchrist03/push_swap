#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;
	size_t	len;

	if (!s)
		return ;
	i = 0;
	len = ft_strlen((const char *)s);
	while (i < len)
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
